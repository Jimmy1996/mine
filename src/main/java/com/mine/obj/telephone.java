package com.mine.obj;

/**
 * telephone类
 * 属性：title、price、area (均为String)
 */
public class telephone {
    public String title;
    public String price;
    public String area;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }
}
